int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (n > i && s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	if (n > i)
	{
		if (s1[i])
			return (s1[i]);
		if (s2[i])
			return (-s2[i]);
	}
	return (0);
}
